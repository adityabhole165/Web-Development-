// Function with Argument
/**
 * values we pass to the function
 * function funcName(args 1, args 2){
 *  // do somethings 
 * }
 */

function printName(name) {
    console.log(name);  
}

function printInfo(name,age) {
    console.log(`${name}'s is ${age}`);  
}

printName("Aditya")
printInfo("Aditya")
printInfo("Arjun",30)